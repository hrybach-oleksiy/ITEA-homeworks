//    один масив - улюблена спортивна команда / моделі автомобілів / міста,
//     у другому – кількість гравців у команді / обсяг двигуна / кількість мешканців у місті.
//    З двох масивів зробіть асоціативний масив(об'єкт).
//    Масив, який виступатиме як ключ - повинен містити унікальні значення

//    Наприклад:
//         let keys = ["id", "name"];
// let values = [1, "Vlad"];

//    В результаті має вийде таке:
// {
//     "id": 1,
