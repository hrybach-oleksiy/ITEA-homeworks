// }

const cars = ['Honda', 'Subaru', 'Mazda', 'Toyota', 'Nissan'];
const engine = [2, 3.2, 1.6, 2.4, 1.8];
const result = {};

for (let i = 0; i < cars.length; i++) {
    result[cars[i]] = engine[i];
}

console.log(result);